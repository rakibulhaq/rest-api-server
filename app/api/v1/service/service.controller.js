const BaseController = require('../base').BaseController;
const ServiceHandler = require('./service.handler');
class ServiceController extends BaseController{
    constructor(){
        super();
        this._ServiceHandler = new ServiceHandler();
        this._passport = require('passport');

    }
    getServiceInfo(req, res, next){
        this.authenticate(req, res, next, ()=>{
            this._ServiceHandler.getServiceInfo(req, this._responseManager.getDefaultResponseHandler(res));
        });

    }
    getAll(req, res, next){
        this.authenticate(req, res, next, ()=>{
            this._ServiceHandler.getAllService(req, this._responseManager.getDefaultResponseHandler(res));

        });

    }
    create(req, res, next){
        this.authenticate(req, res, next, ()=>{
            this._ServiceHandler.createServiceInfo(req, this._responseManager.getDefaultResponseHandler(res));

        });

    }
    update(req, res, next){
        this.authenticate(req, res, next, ()=>{
            this._ServiceHandler.updateService(req, this._responseManager.getDefaultResponseHandler(res));

        });

    }
    remove(req, res, next){
        this.authenticate(req, res, next, ()=>{
            this._ServiceHandler.deleteService(req, this._responseManager.getDefaultResponseHandler(res));

        });

    }
    authenticate(req, res, next, callback){
        let responseManager = this._responseManager;
        this._passport.authenticate('jwt-rs-auth', {
            onVerified: callback,
            onFailure: function(error){
                responseManager.respondWithError(res, error.status || 401 , error.message);
            }
        })(req, res, next);

    }
}

module.exports = ServiceController;